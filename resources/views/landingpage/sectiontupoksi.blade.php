    <h1 class="font-bold  md:text-2xl text-blue-900 text-start mb-3">
        Tugas
    </h1>

    <p class="text-gray-700 text-sm md:text-lg text-justify mb-4">

        Penasihat khusus presiden adalah yang memiliki tugas tertentu yang diberikan oleh presiden di luar tugas-tugas
        yang
        sudah dicakup dalam susunan organisasi kementerian dan instansi pemerintah lainnya. Dalam pelaksanaan tugasnya,
        penasihat khusus presiden bertanggung jawab kepada presiden, serta dikoordinasikan dengan sekretaris kabinet
        (Setkab).
    </p>
    <p class="text-gray-700 text-sm md:text-lg text-justify mb-4">


        Untuk mendukung kelancaran pelaksanaan tugasnya, setiap penasihat khusus presiden dibantu paling banyak 2
        asisten
        dan setiap asisten dibantu paling banyak 2 pembantu asisten. Penasihat khusus presiden dapat berasal dari
        pegawai
        negeri sipil (PNS) atau non-PNS. Hak keuangan dan fasilitas lainnya bagi penasihat khusus presiden diberikan
        setinggi-tingginya setingkat dengan jabatan menteri.
    </p>

